#include <stdio.h>
#include <stdlib.h>
#include <time.h>

void main(){
// definindo variaveis
    int a = 0, b = 10;

// contando até 10 ------------
    while(a <= 10){
//imprimindo 'a' na tela:
    printf("\n %d", a);

//incremento
    a = a + 2;
    }

// Contagem regressiva ----------
    while(b >= 1){
//imprimindo 'b' na tela:
    printf("\n %d", b);

//incremento
    b = b - 2;
    }


}

